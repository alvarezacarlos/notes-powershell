# Paradigmas
# PowerShell es un lenguaje de scripting que admite múltiples paradigmas de programación, aunque se destaca especialmente en la programación orientada a objetos (OOP) y en la automatización de tareas de administración del sistema. A continuación, se enumeran algunos de los paradigmas compatibles con PowerShell:

# Programación Imperativa:
  # PowerShell permite escribir scripts y comandos de manera imperativa, especificando paso a paso cómo deben realizarse las tareas.

# Programación Orientada a Objetos (OOP):
  # PowerShell está diseñado para trabajar con objetos. Los cmdlets y las funciones en PowerShell devuelven y manipulan objetos. También se pueden definir clases y trabajar con conceptos orientados a objetos.

# Programación Funcional:
  # PowerShell incluye características funcionales que permiten el uso de funciones como ciudadanos de primera clase. Se pueden definir funciones anónimas y utilizar funciones de orden superior.

# Scripting y Automatización:
  # Es especialmente fuerte en el paradigma de scripting, permitiendo la creación de scripts para la automatización de tareas del sistema operativo y la administración del entorno Windows.

# Manejo de Eventos:
  # PowerShell incluye soporte para manejar eventos y suscribirse a eventos del sistema.

# Programación Declarativa:
  # En ciertos contextos, como en archivos de configuración de Desired State Configuration (DSC), se utiliza un enfoque más declarativo para describir cómo debe estar configurado un sistema.
  
# PowerShell es un lenguaje versátil que se adapta a una variedad de tareas y escenarios. Los programadores y administradores de sistemas pueden aprovechar distintos paradigmas según sus necesidades y el contexto de uso.